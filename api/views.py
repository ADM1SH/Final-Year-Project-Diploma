class ItemViewSet(viewsets.ModelViewSet):
    """CRUD operations for items. Uses select_related and prefetch_related for optimal performance."""
    # select_related for 1:1 and ForeignKeys, prefetch_related for Many-to-Many and reverse ForeignKeys
    queryset = Item.objects.select_related('seller', 'category').prefetch_related('images').all()
    serializer_class = ItemSerializer
    permission_classes = [permissions.AllowAny]
    
    # Advanced Filtering, Searching, and Ordering
    filterset_fields = ['category', 'calculated_grade', 'is_sold', 'price']
    search_fields = ['name', 'description']
    ordering_fields = ['price', 'created_at']

    def perform_create(self, serializer):
        # Auto-assign the currently authenticated user as the seller
        # Fallback to the first user if anonymous (for initial development ease)
        from django.contrib.auth.models import User
        user = self.request.user
        if user.is_anonymous:
            user = User.objects.first() # Temporary dev hack since Auth isn't enforced yet
        serializer.save(seller=user)


